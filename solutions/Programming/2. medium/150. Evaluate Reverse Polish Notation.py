from collections import deque


def evalRPN(tokens: list[str]) -> int:
    dec = deque()
    for i in tokens:
        #print(i)
        if i.isdigit() or len(i) > 1:
            dec.append(int(i))
        else:
            b = dec.pop()
            a = dec.pop()
            match i:
                case '+':
                    dec.append(a + b)
                case '-':
                    dec.append(a - b)
                case '*':
                    dec.append(a * b)
                case '/':
                    dec.append(int(a / b))
            #print(dec)
    return int(dec.pop())
    
